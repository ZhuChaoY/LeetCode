'''
实现函数 ToLowerCase()，该函数接收一个字符串参数 str，并将该字符串中的大写字母转换成
小写字母，之后返回新的字符串。
输入: "Hello" 输出: "hello"
输入: "here" 输出: "here"
输入: "LOVELY" 输出: "lovely"
'''

def toLowerCase(str):
    return str.lower()

print(toLowerCase('Hello'))
print(toLowerCase('Here'))
print(toLowerCase('LOVELY'))
         
